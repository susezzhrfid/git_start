bool GetProIdInfo(DWORD dwId, TProIdInfo* pInfo, bool fBlk)
{
	const TProId2Sub *pProId2Sub = NULL;
//	TProId2Sub *blkProId = NULL;
//	DWORD dwParentId = 0;
//	DWORD dwMask = 0;
//	DWORD dwOffset = 0;
	bool fFind = false;
//	bool fSubId = false;
//	DWORD dwMaskId = 0;
	BYTE *pbSubId = NULL;
	BYTE *pbBlkId = NULL;
	int i=0;

	memset(pInfo,0,sizeof(TProIdInfo));

	pProId2Sub = BinarySearchProId(dwId);

	if(pProId2Sub != NULL)
	{
		if(pProId2Sub->dwID == dwId)
		{
			//dwId在映射表中存在
			fFind = true;
			pInfo->dwId = dwId;
			pInfo->bIdType = pProId2Sub->wIdType;
			pInfo->wDataLen = GetExistItemsLenDw(&dwId,1) > 0 ? GetExistItemsLenDw(&dwId,1) : 0;
			pInfo->bIdx = 0;
			pInfo->wOffset = 0;
			pInfo->dwProId = pInfo->dwId;
			pInfo->wProLen = pInfo->wDataLen;
			pInfo->bCode = pProId2Sub->bCode;

			if(fBlk && pProId2Sub->dwBlkID != 0)
			{
				fFind = false;
				pProId2Sub = BinarySearchProId(pProId2Sub->dwBlkID);
			}

			if(fFind)
				return fFind;
		}

		if(!fFind && pProId2Sub!=NULL)
		{
			fFind = true;
			//dwId在映射表中不存在,只有对应的块id
			//从块id中取dwId的信息
			pInfo->dwId = dwId;
			pInfo->bIdType = 0;

			//下面这些参数是要计算的。
			pInfo->dwProId = pProId2Sub->dwID;
			//取出块id的数据内容长度
			pInfo->wProLen = GetExistItemsLenDw(&pProId2Sub->dwID,1) > 0 ? GetExistItemsLenDw(&pProId2Sub->dwID,1) : 0;
			pInfo->bCode = pProId2Sub->bCode;
			
			//dwId数据内容长度根据块id的长度除子id个数来求出长度
			if(pProId2Sub->bCode == C_01R) //块id中带费率数
			{
				pInfo->wDataLen = (pInfo->wProLen - 1)/pProId2Sub->bSubNum;

			}
			else
			{
				pInfo->wDataLen = pInfo->wProLen/pProId2Sub->bSubNum;
			}
			//取出单id在块id中的偏移
			pbSubId = (BYTE*)&dwId;
			pbBlkId = (BYTE*)&pProId2Sub->dwID;
			for(i = 0; i < 4; i++)
			{
				if(*pbSubId != *pbBlkId)
				{
					pInfo->bIdx = *pbSubId;
					break;
				}
				pbSubId++;
				pbBlkId++;
			}
			if(pProId2Sub->bCode == C_02)
			{
				//谐波的第二个子ID的最低位从02开始
				if(pInfo->bIdx >= 2)
					pInfo->bIdx -= 1;
			}
			else if(pProId2Sub->bCode == C_03)
			{
				//当前数据中中的 本日 电压偏差最大值、最小值、平均值
				if(pInfo->bIdx >= 1)
					pInfo->bIdx -= 1;
			}

			if(pInfo->bIdx >= 0 && pInfo->bIdx < pProId2Sub->bSubNum)   //pInfo->bIdx >= 0???
			{
				pInfo->wOffset = pInfo->bIdx*pInfo->wDataLen;
				if(pProId2Sub->bCode == C_01R)
					pInfo->wOffset += 1;
			}
			else
			{
				//超出目前所支持的单id个数, 如费率数目前只支持4费率，
				pInfo->bIdx = INVALID_DATA;
				pInfo->wOffset = 0xFFFF;

			}
		}

	}
		
	return fFind;
}

