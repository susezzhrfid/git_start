#define PROID2SUB_NUM	(sizeof(g_ProId2Sub)/sizeof(g_ProId2Sub[0]))


const TProId2Sub* BinarySearchProIdTab(const TProId2Sub* pProId2Sub, WORD num, DWORD dwID)
{
	int little, big, mid;
	if (dwID<pProId2Sub[0].dwID  || dwID>pProId2Sub[num-1].dwID)
		return NULL;

	little = 0;
	big = num;
	while (little <= big)
	{                               
		mid = (little + big) / 2;       //二分

		if (pProId2Sub[mid].dwID == dwID) 
		{
			return pProId2Sub + mid;
		}
		else if (dwID > pProId2Sub[mid].dwID)
		{
			little = mid + 1;
		} 
		else  
		{
			big = mid - 1;
		}

		mid = (little + big) / 2;
	}

	return NULL;
}

const TProId2Sub* BinarySearchProId(DWORD dwID)
{
	const TProId2Sub* pProId2Sub = NULL;
	pProId2Sub = BinarySearchProIdTab(g_ProId2Sub,PROID2SUB_NUM,dwID);
	if(pProId2Sub == NULL)
	{
			//根据id本身搜索未搜到,将DI0位改成0xff,搜索是否存在对应的块id
			pProId2Sub = BinarySearchProIdTab(g_ProId2Sub,PROID2SUB_NUM,dwID|0xff);
			if(pProId2Sub == NULL)
			{
				//在DI0位改成0xff未搜到块id,再将DI1位改成0xff,搜索是否存在对应的块id
				pProId2Sub = BinarySearchProIdTab(g_ProId2Sub,PROID2SUB_NUM,dwID|0xff00);
			}
	}
	return pProId2Sub;
}

