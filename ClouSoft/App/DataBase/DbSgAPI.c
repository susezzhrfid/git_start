bool InitProId2Sub()
{

	int i=0;
	//初始化g_ProId2Sub相关工作

	//上电时调整块ID包括的子ID个数
	for(i = 0; i < PROID2SUB_NUM; i++)
	{
		//验证ID顺序
		if(i > 0)
		{
			if((g_ProId2Sub[i-1].dwID > g_ProId2Sub[i].dwID))
			{
				DTRACE(DB_FAPROTO,("Init ProId2Sub g_ProId2Sub[%d].%08x > g_ProId2Sub[%d].%08x,id order occur err!",i-1,g_ProId2Sub[i-1].dwID,i,g_ProId2Sub[i].dwID));
			}
		}
		if(i < PROID2SUB_NUM-1)
		{
			if((g_ProId2Sub[i].dwID > g_ProId2Sub[i+1].dwID))
			{
				DTRACE(DB_FAPROTO,("Init ProId2Sub g_ProId2Sub[%d].%08x > g_ProId2Sub[%d].%08x,id order occur err!",i,g_ProId2Sub[i].dwID,i+1,g_ProId2Sub[i+1].dwID));
			}
		}
	}

	return true;
}

int WriteItemDw(WORD wBank, WORD wPn, DWORD dwID, BYTE* pbBuf, DWORD dwTime)
{
	WORD *pwSub = NULL;
	WORD wSubNum = 0;
	BYTE *pbData = pbBuf;
	int i=0, iTotalLen = 0;
	int iRet = 0;
	pwSub = ProId2SubId(dwID,&wSubNum);
	
	if(pwSub != NULL && wSubNum > 0)
	{
		WORD wId;
		for(i = 0; i < wSubNum; i++)
		{
			BYTE bModeTest = 0;
			ReadItemEx(BN1,PN0,0x2113,&bModeTest);

			wId = pwSub[i];
			if (BN0 == wBank && PN0 == wPn && wId >= 0x8010 && wId <= 0x802f)//通信参数只设置备份的
			{
				wId = pwSub[i]+0x0870;
			}

			if(0x01==bModeTest && wId==0x8017)//测试模式下, 心跳实时生效
				wId = pwSub[i];

			if((iRet=WriteItemTm(wBank,wPn,wId,pbData,dwTime)) > 0)
			{
				pbData += iRet;
				iTotalLen += iRet;
			}
			else
			{
				iTotalLen = -1;
				break;
			}
		}
	}
	return iTotalLen;
}

int PostReadItemDwHook(WORD wBank, WORD wPn, DWORD dwID, WORD *pwSub, WORD wSubNum, BYTE* pbData, DWORD dwStartTime, DWORD dwEndTime)
{
	BYTE bBuf[64];
	int iRet = 0;

	if (wBank==BN0 && (dwID==0x020a01ff || dwID==0x020a02ff || dwID==0x020a03ff || dwID==0x020b01ff || dwID==0x020b02ff || dwID==0x020b03ff))
	{//需要组合处理的ID，
		if (wSubNum < 2)
			iRet = -1;

		if (ReadItemTm(wBank,wPn,pwSub[0], &bBuf[0],dwStartTime,dwEndTime) != 2 ||
			ReadItemTm(wBank,wPn,pwSub[1], &bBuf[2],dwStartTime,dwEndTime) != 42 )
			return -2;
		
		memcpy(pbData, bBuf, 2);
		memcpy(pbData+2, &bBuf[4], 40);
		iRet = 42;
	}

	return iRet;
}

//单个ID按缓冲区的读
int ReadItemDw(WORD wBank, WORD wPn, DWORD dwID, BYTE* pbBuf, DWORD dwStartTime, DWORD dwEndTime)
{
	WORD *pwSub = NULL;
	WORD wSubNum = 0;
	BYTE bBuf[1024];
	BYTE *pbData = bBuf;
	int i=0, iTotalLen = 0;
	int iRet = 0;
	TProIdInfo ProIdInfo = {0}; //20140605-2

	memset(&ProIdInfo,0,sizeof(TProIdInfo));
	if(!GetProIdInfo(dwID, &ProIdInfo, false))
		return -1;

	pwSub = ProId2SubId(ProIdInfo.dwProId,&wSubNum);

	if(pwSub != NULL && wSubNum > 0)
	{
		//需要做一个特殊处理，对于某些南网协议ID和07-645ID不对应，需要对抄读的07-645ID组合处理
		iRet = PostReadItemDwHook(wBank, wPn, ProIdInfo.dwProId, pwSub, wSubNum, pbData, dwStartTime,dwEndTime);
		if (iRet > 0)
		{
			pbData += iRet;
			iTotalLen += iRet;
			if (ProIdInfo.wDataLen > GetItemLen(wBank,pwSub[0]))
				ProIdInfo.wDataLen -= GetItemLen(wBank,pwSub[0]);//20140320-1
			else
				ProIdInfo.wDataLen = 0;
		}
		else if (iRet == -2)	//确定是读不到了，不用多读一次
		{
			iTotalLen = -1;
		}
		else
		{
			for(i = 0; i < wSubNum; i++)
			{
				int irBitNum = 0;
				iRet = GetItemLen(wBank,pwSub[i]);
				if(wBank==BN0 && IsNorSuportId(pwSub[i]))//20140626-1
					irBitNum = ReadItemEx(wBank,wPn,pwSub[i],pbData);
				else
					irBitNum = ReadItemTm(wBank,wPn,pwSub[i],pbData,dwStartTime,dwEndTime);
				if(iRet>0 && irBitNum==iRet)
				{
					pbData += iRet;
					iTotalLen += iRet;
				}
				else
				{
					iTotalLen = -1;
					break;
				}
			}	
		}
		if(ProIdInfo.bIdx == INVALID_DATA && ProIdInfo.wOffset==0xFFFF) //子id超出目前块id所支持的范围,没赋正确的偏移默认成无效数据
			memset(pbBuf,INVALID_DATA,ProIdInfo.wDataLen);
		else
			memcpy(pbBuf,bBuf+ProIdInfo.wOffset,ProIdInfo.wDataLen);
	}
	return iTotalLen;
}

